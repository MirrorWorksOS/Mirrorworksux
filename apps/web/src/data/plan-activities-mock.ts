/**
 * Seed data for the Plan Activities prototype. Spread across four jobs to
 * exercise the inbox, the per-job tab, the kanban board, and the time
 * tracking summaries. ISO dates anchored to today so the inbox always shows
 * a mix of overdue / due-today / future activities.
 */

import type { JobActivity, TimeEntry } from '@/types/job-activity';

const NOW = new Date();
const todayIso = (offsetDays: number, hour = 9, minute = 0): string => {
  const d = new Date(NOW);
  d.setDate(d.getDate() + offsetDays);
  d.setHours(hour, minute, 0, 0);
  return d.toISOString();
};

const t = (
  id: string,
  activityId: string,
  startOffsetDays: number,
  startHour: number,
  durationMin: number,
  userName: string,
  notes?: string,
): TimeEntry => {
  const startedAt = todayIso(startOffsetDays, startHour, 0);
  const stoppedAt = new Date(new Date(startedAt).getTime() + durationMin * 60_000).toISOString();
  return {
    id,
    activityId,
    userId: `user-${userName.split(' ').join('-').toLowerCase()}`,
    userName,
    startedAt,
    stoppedAt,
    minutes: durationMin,
    notes,
    source: 'timer',
  };
};

